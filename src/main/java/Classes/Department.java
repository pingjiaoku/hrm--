package Classes;

public class Department {
    private Integer depID;
    private String depName;
    private Integer parentDepID;
    private Integer depType;
    private String depAddress;
    private String depPostCode;
    private String depPhoneNumber;
    private String depTelePhone;
    private String depEmail;

    public Integer getDepID() {
        return depID;
    }

    public void setDepID(Integer depID) {
        this.depID = depID;
    }

    public String getDepName() {
        return depName;
    }

    public void setDepName(String depName) {
        this.depName = depName;
    }

    public Integer getParentDepID() {
        return parentDepID;
    }

    public void setParentDepID(Integer parentDepID) {
        this.parentDepID = parentDepID;
    }

    public Integer getDepType() {
        return depType;
    }

    public void setDepType(Integer depType) {
        this.depType = depType;
    }

    public String getDepAddress() {
        return depAddress;
    }

    public void setDepAddress(String depAddress) {
        this.depAddress = depAddress;
    }

    public String getDepPostCode() {
        return depPostCode;
    }

    public void setDepPostCode(String depPostCode) {
        this.depPostCode = depPostCode;
    }

    public String getDepPhoneNumber() {
        return depPhoneNumber;
    }

    public void setDepPhoneNumber(String depPhoneNumber) {
        this.depPhoneNumber = depPhoneNumber;
    }

    public String getDepTelePhone() {
        return depTelePhone;
    }

    public void setDepTelePhone(String depTelePhone) {
        this.depTelePhone = depTelePhone;
    }

    public String getDepEmail() {
        return depEmail;
    }

    public void setDepEmail(String depEmail) {
        this.depEmail = depEmail;
    }
}
